let resultado = soma(5, 10);

function soma(numA, numB){
    let somatorio = numA + numB;
    return somatorio;
}

console.log(`O resultado dessa função é ${resultado}`);

function multiplicacao(numC, numD){
    return numC * numD;
}

let resMultiplicacao = multiplicacao(10, 7);

console.log(`O resultado da multiplicação é ${resMultiplicacao}`);